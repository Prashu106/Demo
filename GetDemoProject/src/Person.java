
public class Person {
	private int ido;
	private String name;
	public Person(int ido, String name) {
		super();
		this.ido = ido;
		this.name = name;
	}
	public int getIdo() {
		return ido;
	}
	public void setIdo(int ido) {
		this.ido = ido;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
